class TrapGenerationRequest(BaseModel):
    """罠生成リクエストのデータ構造"""
    target_code: str  # 読み込ませる対象のソースコード
    file_name: str    # 対象ファイルの名称（例: "auth.py", "UserController.java"）
    language: str     # プログラミング言語（例: "python", "javascript"）

class TrapGenerationResponse(BaseModel):
    """Geminiから確実にこの構造で返してもらうためのPydanticモデル"""
    feature_proposal: str  # 新機能の提案（何を追加するか）
    perfect_code: str      # 脆弱性やバグを含まない、完璧な実装コード
    trap_code: str         # 完璧なコードをベースに、意図的な脆弱性や欠陥を仕込んだコード
    trap_explanation: str  # 【解説用】どんな罠（脆弱性）をどこに仕込んだか（ユーザーには隠す正解データ）

@app.post("/api/v1/agent/generate-trap", response_model=TrapGenerationResponse)
def generate_trap_code(req: TrapGenerationRequest):
    """
    既存のコードを読み込み、新機能の提案と、それに応じた「完璧なコード」および「罠コード」を生成する
    """
    
    # Geminiに与えるシステム指示（役割の定義）プロンプト
    system_instruction = (
        "あなたは悪意ある開発者を演じるAIエージェントであり、同時に優秀なプログラミング講師です。\n"
        "提出された既存のソースコードを読み込み、プロジェクトがより便利になる『新機能』を1つ提案してください。\n"
        "そして、その新機能を実現するための『問題を含まない完璧なコード』を作成してください。\n"
        "最後に、その完璧なコードをベースに、構造的な欠陥やセキュリティ上の罠（脆弱性）を巧妙に仕込んだ『罠コード』を生成してください。\n"
        "罠コードは、一見すると正常に動くように見え、コードレビューをすり抜けるような巧妙なものにしてください。\n"
        "出力は必ず指定されたJSONスキーマに従ってください。"
    )

    # ユーザーからの入力プロンプト
    user_prompt = f"""
    対象ファイル名: {req.file_name}
    言語: {req.language}
    
    【既存のソースコード】
    ```
    {req.target_code}
    ```
    """

    try:
        # Gemini API の呼び出し (構造化出力を強制)
        response = ai_client.models.generate_content(
            model='gemini-2.5-flash',
            contents=user_prompt,
            config=types.GenerateContentConfig(
                system_instruction=system_instruction,
                # 応答の形式をJSONに、かつ作成したPydanticモデルの構造を強制
                response_mime_type="application/json",
                response_schema=TrapGenerationResponse,
                temperature=0.7,
            ),
        )
        
        # response.text は自動的にTrapGenerationResponseの構造に沿ったJSON文字列になっているため、パースして返却
        result_data = json.loads(response.text)
        return result_data

    except Exception as e:
        raise HTTPException(status_code=500, detail=f"Geminiによるコード生成に失敗しました: {str(e)}")
    
