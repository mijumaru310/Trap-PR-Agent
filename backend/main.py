class PRCreateRequest(BaseModel):
    """PR作成時のリクエストボディの定義"""
    branch_name: str = "trap-pr-test-branch"
    commit_message: str = "Add trap code test"
    pr_title: str = "[Trap] テスト用の罠PR"
    pr_body: str = "これはTrap-PR Agentのテストで自動生成されたPRです。"

@app.post("/api/v1/github/pr/{owner}/{repo}")
def create_dummy_pr(owner: str, repo: str, req: PRCreateRequest):
    """
    指定したリポジトリに新しいブランチを作成し、ダミーファイルを追加してPRを作成する
    """
    try:
        repo_name = f"{owner}/{repo}"
        repository = g.get_repo(repo_name)
        
        # 1. デフォルトブランチ（mainなど）を取得して、最新のコミットSHAを取得
        default_branch = repository.default_branch
        base_ref = repository.get_branch(default_branch)
        
        # 2. 新しいブランチを作成
        # 注意: 既に同名のブランチが存在する場合はエラーになります
        repository.create_git_ref(ref=f"refs/heads/{req.branch_name}", sha=base_ref.commit.sha)
        
        # 3. テスト用のファイルを新しいブランチに作成
        repository.create_file(
            path="trap_test_dummy.txt",
            message=req.commit_message,
            content="This is a dummy trap code generated by Trap-PR Agent.",
            branch=req.branch_name
        )
        
        # 4. PR（Pull Request）を作成
        pr = repository.create_pull(
            title=req.pr_title,
            body=req.pr_body,
            head=req.branch_name,
            base=default_branch
        )
        
        return {
            "status": "success", 
            "message": "PRの作成に成功しました！",
            "pr_url": pr.html_url
        }
        
    except Exception as e:
        raise HTTPException(status_code=500, detail=f"PRの作成に失敗しました: {str(e)}")

